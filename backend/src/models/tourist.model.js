import mongoose from "mongoose";

const touristSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    role: {
      type: String,
      default: "tourist",
    },
  },
  { timestamps: true }
);

const Tourist = mongoose.model("Tourist", touristSchema);

export default Tourist;
